package com.tem.app;

/**
 * 
 * This class defines all the variables, setters/getters and constructor
 * for Movies
 * @author ash
 *
 */
public class Movie {

	private String movie;
	
	public Movie(String movie) {
		this.movie = movie;
	}
	
	public void setMovie(String movie) {
		this.movie = movie;
	}
	
	public String getMovie() {
		return movie;
	}
	
}
